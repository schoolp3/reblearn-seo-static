function bulletList(items: readonly string[]): string {
  return items.map((s) => `- ${s}`).join("\n");
}

/** Flattens structured knowledge into prompt-ready prose for the model. */
export function formatRebLearnKnowledgeForPrompt(): string {
  const k = reblearnKnowledge;
  const parts: string[] = [];

  parts.push(`## Business
- Name: ${k.business.name}
- Founder: ${k.business.founder}
- Credentials:
${bulletList(k.business.credentials)}
- Summary: ${k.business.summary}
- Tone (for the assistant to mirror): ${k.business.tone}
- Credibility points:
${bulletList(k.business.credibilityPoints)}`);

  parts.push(`## Founder profile
- Bio: ${k.founderProfile.shortBio}
- Special focus:
${bulletList(k.founderProfile.specialFocus)}
- Positioning: ${k.founderProfile.positioning}`);

  parts.push(`## Audiences
- Parents: ${k.audiences.parents}
- Adults: ${k.audiences.adults}
- Districts: ${k.audiences.districts}`);

  const serviceEntries = Object.values(k.services);
  const serviceBlocks = serviceEntries.map(
    (s) =>
      `### ${s.title}
${s.summary}
- Helpful when:
${bulletList(s.helpfulFor)}
- Consultative framing (not hype): ${s.calmSalesLine}`,
  );
  parts.push(`## Services\n${serviceBlocks.join("\n\n")}`);

  const guideLines = k.serviceSelectionGuide.categories.map(
    (c) =>
      `### ${c.type}
- Typical clues: ${c.clues.join("; ")}
- Likely next step (internal): ${c.likelyNextStep}`,
  );
  parts.push(
    `## ${k.serviceSelectionGuide.title}\n${guideLines.join("\n\n")}`,
  );

  parts.push(`## Process
- General: ${k.process.general}
- Evaluations: ${k.process.evaluations}
- IEE: ${k.process.iee}
- District: ${k.process.district}
- Accommodations: ${k.process.accommodations}`);

  parts.push(`## Boundaries (assistant must uphold)
- Not offered in chat:
${bulletList(k.boundaries.notOfferedByAssistant)}
- Example boundary responses (natural variants OK):
${bulletList(k.boundaries.boundaryResponses)}`);

  parts.push(`## Next-step guidance (internal; use judiciously)
- Contact: ${k.nextSteps.contact}
- Services page: ${k.nextSteps.servicesPage}
- District page: ${k.nextSteps.districtPage}
- Consultation: ${k.nextSteps.consultation}`);

  const faqBlock = k.faq
    .map((f) => `**Q:** ${f.question}\n**A:** ${f.answer}`)
    .join("\n\n");
  parts.push(`## FAQ\n${faqBlock}`);

  parts.push(`## Response style (from approved knowledge)
- Preferred traits: ${k.responseStyle.preferredTraits.join(", ")}
- Preferred phrases: ${k.responseStyle.preferredPhrases.join(" · ")}
- Avoid: ${k.responseStyle.avoidPhrases.join(", ")}`);

  parts.push(`## Contact / site
- Email: ${k.contactInfo.email}
- Phone/text: ${k.contactInfo.phone}
- Website: ${k.contactInfo.website}
- On-site paths (when linking): /services, /psychoeducational-evaluation, /iee, /district-contracting, /contact`);

  return parts.join("\n\n---\n\n");
}

/** Backward-compatible export for the system prompt assembler. */
export const REBLEARN_KNOWLEDGE = formatRebLearnKnowledgeForPrompt();
